"""
train_pinn_aerofoil.py

Minimal PyTorch PINN training script for steady 2D Euler around NACA0012.

- Loads the .npy files generated by generate_aerofoil_training_sets.py
- Builds a small MLP mapping (x,y) -> (rho, u, v, T)
- Computes PDE residual for steady 2D Euler: div G(U) = 0
- Applies shock-adaptive weight lambda based on div(u)
- Loss = L_BC + L_data_internal + L_F
- Optimizes with Adam then LBFGS

Notes:
- No quantum branch is implemented here. If you want HQPINN, you would add a parallel PQC branch and fuse outputs.
- If you have CFD fields, load them and fill arrays for X_data_int + targets.
"""

import numpy as np
import torch
import torch.nn as nn
import torch.optim as optim

# ----------------------------
# Config
# ----------------------------
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
gamma = 1.4
R_gas = 287.0  # only used if working in SI; if nondim, adapt accordingly

# file names produced by generate_aerofoil_training_sets.py
X_in_file = "X_in.npy"
X_out_file = "X_out.npy"
X_top_file = "X_top.npy"
X_bot_file = "X_bot.npy"
X_wall_file = "X_wall.npy"
X_wall_normals_file = "X_wall_normals.npy"
X_f_file = "X_f.npy"
X_data_int_file = "X_data_int.npy"

# training hyperparams
lr = 5e-4
adam_steps = 40000
lbfgs_max_iter = 2000
epsilon_lambda = 0.1  # shock weighting parameter


# ----------------------------
# Utilities: load datasets
# ----------------------------
def load_points(fname):
    return torch.tensor(np.load(fname), dtype=torch.float32, device=device)


X_in = load_points(X_in_file)  # shape (N_in, 2)
X_out = load_points(X_out_file)
X_top = load_points(X_top_file)
X_bot = load_points(X_bot_file)
X_wall = load_points(X_wall_file)
X_wall_normals = torch.tensor(
    np.load(X_wall_normals_file), dtype=torch.float32, device=device
)
X_f = load_points(X_f_file)  # PDE collocation
X_data_int = load_points(
    X_data_int_file
)  # supervised internal (if you have CFD targets)

# ----------------------------
# If you have CFD targets for X_data_int and boundaries, load them here.
# For example, if you received arrays rho_cfd.npy, u_cfd.npy, v_cfd.npy, T_cfd.npy:
#   Y_data_int = torch.stack([rho_cfd, u_cfd, v_cfd, T_cfd], dim=-1)
# If you don't have CFD, set Y_data_int = None
# ----------------------------
Y_data_int = None
# Example placeholder: if you had a file "Y_data_int.npy" with shape (N_data_int,4)
